function isBuffered(BufferLimit) {

var vbLimit = BufferLimit;
if(BufferLimit<1){
vbLimit = 1;
}
if(BufferLimit>99){
vbLimit = 99;
}
var BGvideo = document.getElementById("vidbgcomp");
